public class MakingGameOneHand {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c = 0;
        
        System.out.println(a + " " + b + " " + c);

        a = 0;
        b = 1;
        c = 2;
        
        System.out.println(a + " " + b + " " + c);
        
        a = 2;
        b = 0;
        c = 1;
        
        System.out.println(a + " " + b + " " + c);
        
       
    }
}
